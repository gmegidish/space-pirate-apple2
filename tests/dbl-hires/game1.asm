ORG $200

CLRPAGE2	EQU	$C054	; TURN OFF PAGE 2 (READ)
SETPAGE2	EQU	$C055	; TURN ON PAGE 2 (READ)
CLR80COL	EQU	$C000	; TURN OFF 80 STORE (WRITE)
SET80COL	EQU	$C001	; TURN ON 80 STORE (WRITE)
CLRRAMRD	EQU	$C002
SETRAMRD	EQU	$C003
CLRRAMWRT	EQU	$C004
SETRAMWRT	EQU	$C005
CLRMIXED	EQU	$C052
SET80VID	EQU	$C00D
SETAN3   	EQU	$C05E 
GR       	EQU	$C050
HIRES    	EQU	$C057

AUXMOVE   	EQU	$C311  ;carry=1 main>aux carry=0 aux>main
STARTLO   	EQU	$3C
STARTHI   	EQU	$3D
ENDLO     	EQU	$3E
ENDHI    	EQU	$3F
DESTINATIONLO	EQU	$42
DESTINATIONHI	EQU	$43

START	
	; MOVE FROM MEM:$4000 TO AUX:$2000
	LDA	#$00
	STA	STARTLO
	STA	ENDLO
	STA	DESTINATIONLO

	LDA	#$20
	STA	DESTINATIONHI

	LDA	#$40
	STA	STARTHI

	LDA	#$60
	STA	ENDHI
	
	SEC
	JSR	AUXMOVE

	; SET DBL HIRES
	LDA	HIRES
	LDA	GR
	LDA	SETAN3	
	STA	SET80VID

	STA	CLR80COL  
	STA	CLRRAMRD
	STA	CLRRAMWRT


FOOBAR	JMP	FOOBAR


	RTS

ORG $2000

INCBIN "PIC1.X"

ORG $4000

