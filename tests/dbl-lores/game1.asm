ORG $200

CLRPAGE2	EQU	$C054	; TURN OFF PAGE 2 (READ)
SETPAGE2	EQU	$C055	; TURN ON PAGE 2 (READ)
CLR80COL	EQU	$C000	; TURN OFF 80 STORE (WRITE)
SET80COL	EQU	$C001	; TURN ON 80 STORE (WRITE)
CLRRAMRD	EQU	$C002
SETRAMRD	EQU	$C003
CLRRAMWRT	EQU	$C004
SETRAMWRT	EQU	$C005
CLRMIXED	EQU	$C052
SET80VID	EQU	$C00D
SETAN3   	EQU	$C05E 
GR       	EQU	$C050
HIRES    	EQU	$C057

AUXMOVE   	EQU	$C311  ;carry=1 main>aux carry=0 aux>main
STARTLO   	EQU	$3C
STARTHI   	EQU	$3D
ENDLO     	EQU	$3E
ENDHI    	EQU	$3F
DESTINATIONLO	EQU	$42
DESTINATIONHI	EQU	$43

START	
	; SET DBL GR
	LDA	GR
	LDA	$C053
	LDA	SETAN3
	LDA	CLRMIXED
	STA	SET80VID

	; MOVE FROM MEM:$800 TO AUX:$400
	LDA	#$0
	STA	STARTLO
	STA	ENDLO
	STA	DESTINATIONLO

	LDA	#$4
	STA	DESTINATIONHI

	LDA	#$8
	STA	STARTHI

	LDA	#$C
	STA	ENDHI
	
	SEC
	JSR	AUXMOVE

FOOBAR	JMP	FOOBAR

	RTS

ORG $400

